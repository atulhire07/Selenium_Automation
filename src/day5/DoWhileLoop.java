package day5;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		//1-----10 numbers print
		
		/*
		 * int i=1;
		 * 
		 * do { System.out.println(i); i++; }while (i<=10);
		 */
		
 
		// 10,9,8,-----1-- descending order
		
		int i=10;
		
		do
		{
			System.out.println(i);
			i--;
			
		}while(i>0);
	}

}
